L=float(input("ingresar el Alto: "))
H=float(input("ingresar el hancho: "))

areaRectangulo=L*H
print(f"El area del rectangulo: {areaRectangulo}")

perimetroRectangulo=2*(L+H)
print(f"El Perometro del rectangulo: {perimetroRectangulo}")

superficie=L*H
print(f"La superficie del rectangulo: {superficie}")